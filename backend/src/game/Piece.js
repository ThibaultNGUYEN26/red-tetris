export default class Piece {
  constructor(type) {
    this.type = type;
    // I piece spawns vertically (rotation 1), all others spawn at rotation 0
    this.rotation = type === 'I' ? 1 : 0;
    this.shape = SHAPES[type][this.rotation];
    this.y = 0;

    // Spawn the piece in the center based on its width
    this.x = Math.floor((BOARD_WIDTH - this.shape[0].length) / 2);
  }

  canSpawn(board) {
    for (let y = 0; y < this.shape.length; y++) {
      for (let x = 0; x < this.shape[y].length; x++) {
        if (this.shape[y][x] === 0) continue;

        const boardX = this.x + x;
        const boardY = this.y + y;

        // Outside board
        if (
          boardX < 0 ||
          boardX >= 10 ||
          boardY < 0 ||
          boardY >= 20
        ) {
          return false;
        }

        // Collision with existing blocks
        if (board[boardY][boardX] !== "empty") {
          return false;
        }
      }
    }
    return true;
  }

  rotate() {
    const rotations = SHAPES[this.type];
    this.rotation = (this.rotation + 1) % rotations.length;
    this.shape = rotations[this.rotation];
  }

  // Get all occupied cells on the board
  getCells() {
    const cells = [];
    for (let row = 0; row < this.shape.length; row++) {
      for (let col = 0; col < this.shape[row].length; col++) {
        if (this.shape[row][col]) {
          cells.push({ x: this.x + col, y: this.y + row });
        }
      }
    }
    return cells;
  }
}
